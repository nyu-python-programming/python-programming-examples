# define a function called 'foo'
def foo():
    print(2 + 2)
    print(5 * 10)
    print(16 / 4)


# define a function called 'main'
def main():
    # this function encapsulates the following three commands
    print("Hello " + "world!")
    print("Goodbye" + " world!")
    print("Hello again" + "!")


# call (i.e. run or invoke) the functions... they will be executed in the order you call them.
main()
foo()
